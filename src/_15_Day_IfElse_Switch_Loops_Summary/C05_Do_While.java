package _15_Day_IfElse_Switch_Loops_Summary;

public class C05_Do_While {
    public static void main(String[] args) {

        // DO WHILE

        int i=0;

        do {
            System.out.println("Merhaba");    // kesinlikle bir kere yazdirir. sonra donguye gider.
            i++;  // 1-2
        } while (i<3);




    }
}

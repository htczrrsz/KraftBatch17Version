package _15_Day_IfElse_Switch_Loops_Summary;

public class C10_Calisma16CarpimTablosu {
    public static void main(String[] args) {

        for (int i=1; i<=9; i++){
            System.out.println("9*" + i + " = " + 9*i);
        }

        System.out.println();

        int i=1;
        while (i<=9){
            System.out.println("9*" + i + "=" + 9*i);
            i++;
        }

        System.out.println();


        int k=1;
        do{
            System.out.println(9*k);
            k++;
        }while (k<=9);



    }
}

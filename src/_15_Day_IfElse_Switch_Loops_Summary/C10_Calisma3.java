package _15_Day_IfElse_Switch_Loops_Summary;

public class C10_Calisma3 {
    public static void main(String[] args) {

        for (int i=15; i<=100; i++){
            System.out.print(i+" ");
        }


        int i=15;
        while (i<=100){
            System.out.print(i+" ");
            i++;
        }




    }
}

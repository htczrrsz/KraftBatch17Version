package _15_Day_IfElse_Switch_Loops_Summary;

public class C10_Calisma1 {
    public static void main(String[] args) {


        for (int i=0; i<=20; i++){
            System.out.println("Merhaba dunya");
        }

        System.out.println();

        int i=0;
        while (i<=20){
            System.out.println("Merhaba dunya");
            i++;
        }

        System.out.println();











    }
}

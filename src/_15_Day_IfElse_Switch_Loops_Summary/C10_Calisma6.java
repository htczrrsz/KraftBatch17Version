package _15_Day_IfElse_Switch_Loops_Summary;

public class C10_Calisma6 {
    public static void main(String[] args) {

        for (int i=1; i<=100; i+=2){
            System.out.print(i+" ");
        }

        System.out.println();

        int i=1;
        while (i<=100){
            System.out.print(i+" ");
            i+=2;
        }



    }
}

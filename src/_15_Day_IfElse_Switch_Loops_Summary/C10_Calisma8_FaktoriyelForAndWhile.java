package _15_Day_IfElse_Switch_Loops_Summary;

public class C10_Calisma8_FaktoriyelForAndWhile {
    public static void main(String[] args) {

        int faktoriyel=1;
        for (int i=1; i<=6; i++){

            faktoriyel=faktoriyel*i;
        }
        System.out.print(faktoriyel);

        System.out.println();

        System.out.println();

        int faktoriyel1=1;
        int i=1;
        while (i<=6){
            faktoriyel1=faktoriyel1*i;
            i++;
        }System.out.print(faktoriyel1);



    }
}

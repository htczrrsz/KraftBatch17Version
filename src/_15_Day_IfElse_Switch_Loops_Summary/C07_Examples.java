package _15_Day_IfElse_Switch_Loops_Summary;

public class C07_Examples {
    public static void main(String[] args) {

        for ( int i= 100; i>0 ; i-=2 ){
            System.out.print (i+" ");
        }

        System.out.println();

        int i=100;
        while (i>0){
            System.out.print(i + " ");
            i-=2;
        }




    }
}
